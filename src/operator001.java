
public class operator001 {
	public static void main(String args[]) {
		int sum1 = 50+100;
		int sum2 = sum1+450;
		int sum3 = sum2+sum2;
		
		System.out.println(sum1);
		System.out.println(sum2);
		System.out.println(sum3);
		System.out.println("-------------------");
		
		int x = 12;
		int y = 3;
		System.out.println(x+y);
		System.out.println(x-y);
		System.out.println(x*y);
		System.out.println(x/y);
		System.out.println(x%y);
		System.out.println("-------------------");
		
		int a = 8;
		int b = 5;
		++a;
		--b;
		System.out.println(a);
		System.out.println(b);
		System.out.println("-------------------");
		
		
	}
}
